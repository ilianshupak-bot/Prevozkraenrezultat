function changeLanguage(lang) {
    localStorage.setItem('preferredLang', lang);
    const selectedData = translations[lang];

    for (let key in selectedData) {
        let element = document.getElementById(key);
        if (element) {
             if(element.tagName === 'INPUT' && (element.type === 'submit' || element.type === 'button')) {
                element.value = selectedData[key];
            } else if (element.tagName === 'OPTION') {
                element.innerText = selectedData[key];
            } else if (element.tagName === 'A' || element.tagName === 'BUTTON') {
                 // –ò–∑–ø–æ–ª–∑–≤–∞–º–µ innerHTML, –∑–∞ –¥–∞ –Ω–µ –∑–∞–≥—É–±–∏–º —Ñ–æ—Ä–º–∞—Ç–∏—Ä–∞–Ω–µ—Ç–æ –ø—Ä–∏ –¥—ä–ª–≥–∏—Ç–µ —Ç–µ–∫—Å—Ç–æ–≤–µ
                 element.innerHTML = selectedData[key];
            } else {
                element.innerHTML = selectedData[key];
            }
        }
    }
}

